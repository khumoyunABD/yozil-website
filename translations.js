// Language switching functionality
class LanguageManager {
  constructor() {
    this.currentLanguage = 'ru'; // Default language is Russian
    this.init();
  }

  init() {
    // Load saved language or use default
    const savedLanguage = localStorage.getItem('yozil-language') || 'ru';
    this.setLanguage(savedLanguage);
    
    // Setup event listeners
    this.setupEventListeners();
  }

  setupEventListeners() {
    const languageBtn = document.getElementById('languageBtn');
    const languageDropdown = document.getElementById('languageDropdown');
    const languageOptions = document.querySelectorAll('.language-option');

    // Toggle dropdown
    languageBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      languageDropdown.classList.toggle('active');
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', () => {
      languageDropdown.classList.remove('active');
    });

    // Handle language selection
    languageOptions.forEach(option => {
      option.addEventListener('click', (e) => {
        e.stopPropagation();
        const selectedLang = option.getAttribute('data-lang');
        this.setLanguage(selectedLang);
        languageDropdown.classList.remove('active');
      });
    });
  }

  setLanguage(lang) {
    if (!translations[lang]) return;
    
    this.currentLanguage = lang;
    localStorage.setItem('yozil-language', lang);
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
      const key = element.getAttribute('data-i18n');
      if (translations[lang][key]) {
        element.innerHTML = translations[lang][key];
      }
    });
    
    // Update language button
    this.updateLanguageButton(lang);
  }

  updateLanguageButton(lang) {
    const languageBtn = document.getElementById('languageBtn');
    const flagMap = {
      'ru': '🇷🇺',
      'uz': '🇺🇿',
      'en': '🇺🇸'
    };
    
    const langNames = {
      'ru': 'Русский',
      'uz': "O'zbek",
      'en': 'English'
    };
    
    if (languageBtn) {
      languageBtn.innerHTML = `
        <span class="flag-icon">${flagMap[lang]}</span>
        <span>${langNames[lang]}</span>
        <span class="dropdown-arrow">▼</span>
      `;
    }
  }

  getCurrentLanguage() {
    return this.currentLanguage;
  }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
  window.languageManager = new LanguageManager();
});